public class ShiftOperators {

    public static void main(String[] args) {
        

        int a=10,b=-10;
        System.out.println(a<<2);
        System.out.println(a>>1);
        System.out.println(b>>1);
        System.out.println(a>>>1); // it works same a>>1
        System.out.println(b>>>1);
    }
    
}
